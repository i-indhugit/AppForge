import time
import json
import copy
from typing import Tuple, List
from app.schemas import (
  FullSchema,
  ValidationReport,
  RepairReport,
  RepairAttempt,
  RepairPatch,
  CompilerOutput,
  DBColumn
)
from app.services.gemini import gemini_service
from app.pipeline.stage5_validator import validate_schemas

def programmatically_patch(schemas: FullSchema, report: ValidationReport) -> Tuple[FullSchema, List[RepairPatch]]:
  """Programmatically generates diff-based targeted patches and applies them to the schemas."""
  repaired_schemas = copy.deepcopy(schemas)
  patches = []
  
  db_tables = {t.name.lower(): t for t in repaired_schemas.db.tables}
  entity_to_table_map = {}
  for table_name in db_tables:
    entity_to_table_map[table_name] = table_name
    if table_name.endswith("s"):
      entity_to_table_map[table_name[:-1]] = table_name

  for error in report.errors:
    err_type = error.type
    loc = error.location
    reason = error.reason

    # 1. UI-API field mismatch repair
    if err_type == "field_mismatch" and "ui.pages" in loc:
      try:
        # Extract page name & missing field name
        page_name = loc.split("ui.pages[")[1].split("]")[0].lower()
        field_name = reason.split("'")[1]
        
        # Find endpoint to patch
        for idx, ep in enumerate(repaired_schemas.api.endpoints):
          if ep.method in ["POST", "PUT"] and page_name in ep.path.lower():
            old_body = copy.deepcopy(ep.request_body_schema)
            if ep.request_body_schema is None:
              ep.request_body_schema = {}
            ep.request_body_schema[field_name] = "string" # default type
            
            patches.append(RepairPatch(
              location=f"api.endpoints[{idx}].request_body_schema",
              before=json.dumps(old_body),
              after=json.dumps(ep.request_body_schema),
              reason=f"Added UI-bound field '{field_name}' to endpoint '{ep.method} {ep.path}' to align interface inputs.",
              error_type=err_type
            ))
            break
      except Exception as e:
        print(f"Failed patching UI-API mismatch: {e}")

    # 2. API-DB field mismatch repair
    elif err_type == "field_mismatch" and "api.endpoints" in loc:
      try:
        # Location format: api.endpoints[idx].request_body_schema.field_name
        field_name = loc.split("request_body_schema.")[1]
        idx = int(loc.split("api.endpoints[")[1].split("]")[0])
        ep = repaired_schemas.api.endpoints[idx]
        
        # Resolve table name from endpoint path
        path_parts = [p for p in ep.path.lower().split("/") if p and p != "api" and p != "auth"]
        if path_parts:
          target_entity = path_parts[0]
          target_table = entity_to_table_map.get(target_entity)
          if target_table:
            # Find DB table object
            t_idx = next((i for i, t in enumerate(repaired_schemas.db.tables) if t.name.lower() == target_table), None)
            if t_idx is not None:
              table_obj = repaired_schemas.db.tables[t_idx]
              old_cols = [c.model_dump() for c in table_obj.columns]
              
              # Add column
              new_col = DBColumn(
                name=field_name,
                type="string",
                nullable=True,
                reason=f"Auto-generated column matching endpoint parameter {field_name}"
              )
              table_obj.columns.append(new_col)
              new_cols = [c.model_dump() for c in table_obj.columns]
              
              patches.append(RepairPatch(
                location=f"db.tables[{t_idx}].columns",
                before=json.dumps(old_cols),
                after=json.dumps(new_cols),
                reason=f"Appended column '{field_name}' to table '{table_obj.name}' to align database schema with API parameter mapping.",
                error_type=err_type
              ))
      except Exception as e:
        print(f"Failed patching API-DB mismatch: {e}")

    # 3. DB Table mismatch repair
    elif err_type == "orphan_api_endpoint":
      try:
        # Create missing database table matching API path resource
        # Endpoint e.g. POST /api/deals -> deals
        idx = int(loc.split("api.endpoints[")[1].split("]")[0])
        ep = repaired_schemas.api.endpoints[idx]
        path_parts = [p for p in ep.path.lower().split("/") if p and p != "api" and p != "auth"]
        if path_parts:
          target_entity = path_parts[0]
          old_tables = [t.name for t in repaired_schemas.db.tables]
          
          # Append new table
          from app.schemas import DBTable
          new_table = DBTable(
            name=target_entity,
            columns=[
              DBColumn(name="id", type="integer", primary_key=True, reason="Autogenerated Primary Key"),
              DBColumn(name="user_id", type="integer", foreign_key="users.id", reason="Links to user owner"),
              DBColumn(name="name", type="string", nullable=False, reason="Name attribute"),
              DBColumn(name="created_at", type="datetime", nullable=True, reason="Creation timestamp")
            ],
            reason=f"Auto-generated database table to satisfy API resource endpoint {ep.path}"
          )
          repaired_schemas.db.tables.append(new_table)
          entity_to_table_map[target_entity] = target_entity
          
          patches.append(RepairPatch(
            location="db.tables",
            before=json.dumps(old_tables),
            after=json.dumps([t.name for t in repaired_schemas.db.tables]),
            reason=f"Synthesized missing database table '{target_entity}' to satisfy API resource bindings.",
            error_type=err_type
          ))
      except Exception as e:
        print(f"Failed patching DB Table mismatch: {e}")

    # 4. Auth Role mismatch repair
    elif err_type == "role_mismatch":
      try:
        # Extract role name
        role_name = reason.split("'")[1]
        old_roles = [r.name for r in repaired_schemas.auth.roles]
        
        # Append missing role
        from app.schemas import AuthRole
        repaired_schemas.auth.roles.append(AuthRole(
          name=role_name,
          permissions=["dashboard:view"],
          reason=f"Auto-healed user permissions definition for role '{role_name}'"
        ))
        
        patches.append(RepairPatch(
          location="auth.roles",
          before=json.dumps(old_roles),
          after=json.dumps([r.name for r in repaired_schemas.auth.roles]),
          reason=f"Appended missing Auth role '{role_name}' into Auth roles matrix.",
          error_type=err_type
        ))
      except Exception as e:
        print(f"Failed patching Role mismatch: {e}")

  return repaired_schemas, patches

def run_stage(output: CompilerOutput) -> CompilerOutput:
  start_time = time.time()
  
  if output.status != "REPAIRING" or not output.schemas or not output.validation:
    output.logs.append("[Stage 6: Repair] Skipping repair because schema is already valid or missing.")
    output.repair = RepairReport(repair_count=0, status="none")
    return output

  output.logs.append("[Stage 6: Repair] Initiating diff-based targeted patching system...")
  
  repair_count = 0
  history = []
  current_schemas = output.schemas
  current_report = output.validation
  
  roles = output.intent.roles if output.intent else []
  services = output.ir.services if output.ir else []
  entities = [e.name for e in output.ir.entities] if output.ir else []
  pages = output.ir.pages if output.ir else []

  # Iterative Targeted Patching Loop
  while current_report.status == "fail" and repair_count < 3:
    repair_count += 1
    output.logs.append(f"[Stage 6: Repair] Patch Attempt #{repair_count}...")
    
    # Run programmatic patch
    patched_schemas, patches_applied = programmatically_patch(current_schemas, current_report)
    
    # Re-validate
    new_report = validate_schemas(patched_schemas, roles, services, entities, pages)
    success = new_report.status == "pass"
    
    history.append(RepairAttempt(
      attempt=repair_count,
      patches=patches_applied,
      success=success
    ))
    
    output.logs.append(f"  * Patch Attempt #{repair_count} complete. Success: {success}. Applied {len(patches_applied)} patches.")
    for p in patches_applied:
      output.logs.append(f"    - PATCH Applied at {p.location}: {p.reason}")

    current_schemas = patched_schemas
    current_report = new_report
    
  latency = (time.time() - start_time) * 1000
  
  # Save patched schemas and validation report back
  output.schemas = current_schemas
  output.validation = current_report
  
  status_val = "repaired" if current_report.status == "pass" else "failed"
  output.repair = RepairReport(
    repair_count=repair_count,
    history=history,
    status=status_val
  )
  output.latency_ms["repair"] = latency
  
  if current_report.status == "pass":
    # Transition to success state internally (verification will override if it fails)
    output.status = "SUCCESS"
    output.logs.append(f"[Stage 6: Repair] Repair successful. Transitioning state to SUCCESS. Latency: {latency:.1f}ms")
  else:
    output.status = "FAILED"
    output.logs.append(f"[Stage 6: Repair] Repair FAILED after 3 attempts. Transitioning state to FAILED. Latency: {latency:.1f}ms")
    
  return output
